package com.test.java;

public class Operator {

	public static void main(String[] args) {
		int a = 10;
		int b = 3;
		System.out.printf("%d + %d = %d%n", a, b, a + b);
		System.out.printf("%d - %d = %d%n", a, b, a - b);
		System.out.printf("%d * %d = %d%n", a, b, a * b);
		System.out.printf("%d / %d = %d%n", a, b, a / b);
		System.out.printf("%d %% %d = %d%n", a, b, a % b);
		System.out.println("10 % 3 = " + 10 % 3);
		System.out.println(10.0/3);

	}

}
