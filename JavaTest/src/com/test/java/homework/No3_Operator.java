package com.test.java.homework;

public class No3_Operator {

	public static void main(String[] args) {
		
		//연산자
		//문장: int sum = 10 + 20;
		//연산자: =, +
		//피연산자: sum, 10, 20
		//표현식: sum = 30, 10 + 20, int sum
		
		//1. 산술연산자
		//+, -, *, /, %(mod)
		//이항 연산자
		
		System.out.printf("%d + %d = %d%n", 3, 1, 3 + 1);
		System.out.printf("%d - %d = %d%n", 3, 1, 3 - 1);
		System.out.printf("%d * %d = %d%n", 3, 1, 3 * 1);
		System.out.printf("%d / %d = %d%n", 3, 1, 3 / 1);
		System.out.printf("%d %% %d = %d%n", 3, 1, 3 % 1);
		
		//나누기 연산자
		//숫자 중 하나 이상이 실수이면 실수의 값으로 출력된다.
		System.out.printf("5.0 / 2.0 = %f%n", 5.0 / 2.0);
		
	

	}

}
